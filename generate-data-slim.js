#!/usr/bin/env node
// ================================================================
// Generate data-slim.js — stripped-down business data for listing pages.
// Only includes the 6 fields used by related.js:
//   id, name, city, state, types, rating
// Run: node generate-data-slim.js
// ================================================================

const fs = require('fs');
const path = require('path');

const dataJs = fs.readFileSync('./public/js/data.js', 'utf-8');
const match = dataJs.match(/const BUSINESSES = (\[[\s\S]*?\n\]);/);
if (!match) {
  console.error('❌ Could not parse BUSINESSES from data.js');
  process.exit(1);
}

const businesses = JSON.parse(match[1]);

const slim = businesses.map(b => ({
  id:     b.id,
  name:   b.name,
  city:   b.city,
  state:  b.state,
  types:  b.types || [],
  rating: b.rating || 0
}));

const output = '// 3DPrintMap slim business data — id, name, city, state, types, rating only\n' +
  '// Generated by generate-data-slim.js — do not edit manually\n' +
  'const BUSINESSES = ' + JSON.stringify(slim) + ';\n';

fs.writeFileSync('./public/js/data-slim.js', output, 'utf-8');

const fullSize  = Buffer.byteLength(dataJs,   'utf-8');
const slimSize  = Buffer.byteLength(output,   'utf-8');
const reduction = (((fullSize - slimSize) / fullSize) * 100).toFixed(1);

console.log('✅ public/js/data-slim.js generated');
console.log('   Full data.js:     ' + (fullSize  / 1024).toFixed(0) + ' KB');
console.log('   Slim data-slim.js:' + (slimSize  / 1024).toFixed(0) + ' KB');
console.log('   Size reduction:   ' + reduction + '%');
console.log('\nNext: node update-listing-scripts.js');
